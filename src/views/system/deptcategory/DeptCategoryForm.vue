<template>
  <Dialog :title="dialogTitle" v-model="dialogVisible">
    <el-form
      ref="formRef"
      :model="formData"
      :rules="formRules"
      label-width="100px"
      v-loading="formLoading"
    >
      <el-form-item label="上级分类" prop="parentId">
        <el-tree-select
          v-model="formData.parentId"
          :data="deptCategoryTree"
          :props="defaultProps"
          check-strictly
          filterable
          default-expand-all
          placeholder="请选择上级分类"
          class="!w-full"
        />
      </el-form-item>
      <el-form-item label="分类名称" prop="name">
        <el-input v-model="formData.name" placeholder="请输入分类名称" />
      </el-form-item>
      <el-form-item label="分类编码" prop="code">
        <el-input v-model="formData.code" placeholder="请输入分类编码" />
      </el-form-item>
      <el-form-item label="排序号" prop="sortOrder">
        <el-input-number v-model="formData.sortOrder" :min="0" :max="9999" controls-position="right" class="!w-full" />
      </el-form-item>
      <el-form-item label="状态" prop="status">
        <el-radio-group v-model="formData.status">
          <el-radio
            v-for="dict in getIntDictOptions(DICT_TYPE.COMMON_STATUS)"
            :key="dict.value"
            :value="dict.value"
          >
            {{ dict.label }}
          </el-radio>
        </el-radio-group>
      </el-form-item>
    </el-form>
    <template #footer>
      <el-button @click="submitForm" type="primary" :disabled="formLoading">确 定</el-button>
      <el-button @click="dialogVisible = false">取 消</el-button>
    </template>
  </Dialog>
</template>
<script setup lang="ts">
import { DICT_TYPE, getIntDictOptions } from '@/utils/dict'
import { DeptCategoryApi, DeptCategoryVO } from '@/api/system/deptcategory'
import { defaultProps, handleTree } from '@/utils/tree'

/** 机构分类字典 表单 */
defineOptions({ name: 'DeptCategoryForm' })

const { t } = useI18n() // 国际化
const message = useMessage() // 消息弹窗

const dialogVisible = ref(false) // 弹窗的是否展示
const dialogTitle = ref('') // 弹窗的标题
const formLoading = ref(false) // 表单的加载中：1）修改时的数据加载；2）提交的按钮禁用
const formType = ref('') // 表单的类型：create - 新增；update - 修改
const formData = ref({
  id: undefined,
  code: undefined,
  name: undefined,
  parentId: 0,
  sortOrder: 0,
  status: 0,
})
const formRules = reactive({
  name: [{ required: true, message: '分类名称不能为空', trigger: 'blur' }],
  code: [{ required: true, message: '分类编码不能为空', trigger: 'blur' }],
})
const formRef = ref() // 表单 Ref
const deptCategoryTree = ref() // 树形结构

/** 打开弹窗 */
const open = async (type: string, id?: number, parentId?: number) => {
  dialogVisible.value = true
  dialogTitle.value = t('action.' + type)
  formType.value = type
  resetForm()
  // 修改时，设置数据
  if (id) {
    formLoading.value = true
    try {
      formData.value = await DeptCategoryApi.getDeptCategory(id)
    } finally {
      formLoading.value = false
    }
  }
  // 新增子分类时，设置父级ID
  if (parentId) {
    formData.value.parentId = parentId
  }
  await getDeptCategoryTree()
}
defineExpose({ open }) // 提供 open 方法，用于打开弹窗

/** 提交表单 */
const emit = defineEmits(['success']) // 定义 success 事件，用于操作成功后的回调
const submitForm = async () => {
  // 校验表单
  await formRef.value.validate()
  // 提交请求
  formLoading.value = true
  try {
    const data = formData.value as unknown as DeptCategoryVO
    if (formType.value === 'create') {
      await DeptCategoryApi.createDeptCategory(data)
      message.success(t('common.createSuccess'))
    } else {
      await DeptCategoryApi.updateDeptCategory(data)
      message.success(t('common.updateSuccess'))
    }
    dialogVisible.value = false
    // 发送操作成功的事件
    emit('success')
  } finally {
    formLoading.value = false
  }
}

/** 重置表单 */
const resetForm = () => {
  formData.value = {
    id: undefined,
    code: undefined,
    name: undefined,
    parentId: 0,
    sortOrder: 0,
    status: 0,
  }
  formRef.value?.resetFields()
}

/** 获得机构分类字典树 */
const getDeptCategoryTree = async () => {
  deptCategoryTree.value = []
  const data = await DeptCategoryApi.getDeptCategoryList({})
  const root: Tree = { id: 0, name: '顶级分类', children: [] }
  root.children = handleTree(data, 'id', 'parentId')
  deptCategoryTree.value.push(root)
}
</script>